var owl = jQuery('.owl-carousel');
owl.on('DOMMouseScroll','.owl-stage',function(e){
    if (e.originalEvent.detail > 50){ 
        owl.trigger('next.owl');
        } else {
        owl.trigger('prev.owl');
    }
    e.preventDefault();
    });

