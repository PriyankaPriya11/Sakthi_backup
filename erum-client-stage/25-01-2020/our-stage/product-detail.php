<script title="cc-theme-script">
    jQuery(document).ready(function() {
        jQuery('.module-360 .model-container .version-container:eq(0)').addClass('active');
        jQuery('.module-360 .model-container .version-container').each(function() {
            jQuery(this).find('.single-image:eq(3)').addClass('active');
        });
        jQuery('.module-360 .model-container .version-container .single-image').on('dragstart', function(event) {
            event.preventDefault();
        });
        jQuery('.module-360 .colors .color-button:eq(0)').addClass('active');
        jQuery('.module-360 .colors .color-button').each(function() {
            jQuery(this).on('click', function() {
                if (!jQuery(this).hasClass('active')) {
                    nextVersion = jQuery(this).attr('adc-value');
                    jQuery('.module-360 .colors .color-button').removeClass('active');
                    jQuery(this).addClass('active');
                    if (nextVersion > currentVersion) {
                        jQuery('.module-360 .model-container .version-container:eq(' +
                            currentVersion + ')').removeClass('active').addClass('prev');
                    } else {
                        jQuery('.module-360 .model-container .version-container:eq(' +
                            currentVersion + ')').removeClass('active').addClass('next');
                    }

                    jQuery('.module-360 .model-container .version-container:eq(' + nextVersion +
                        ')').addClass('active');

                    setTimeout(function() {
                        jQuery('.module-360 .model-container .version-container:eq(' +
                            currentVersion + ')').removeClass('next').removeClass(
                            'prev');
                        currentVersion = nextVersion;
                    }, 500);
                }
            });
        });
        jQuery('.module-360 .model-container .version-container').each(function() {
            var current360container = jQuery(this);
            var imagesCounter = 0;
            var currentImage = 3;
            var nextImage;
            var totalImages = current360container.find('.single-image').length;
            var startingX;
            current360container.find('.single-image').each(function() {
                jQuery(this).attr('adc-value', imagesCounter);
                imagesCounter++;
            });
            current360container.bind('mousedown touchstart', function(event) {
                if (event.pageX) {
                    startingX = event.pageX;
                } else {
                    startingX = event.originalEvent.touches[0].pageX;
                }
                jQuery(window).bind('mousemove touchmove', function(e) {
                    dragOn360 = true;

                    if (e.pageX) {
                        var x = e.pageX;
                    } else {
                        var x = e.originalEvent.touches[0].pageX;
                    }
                    move360model(current360container, x);
                });
                return false;
            });

            function move360model(container, x) {
                if (dragOn360) {
                    if (x < (startingX - 10)) {
                        if (currentImage == (totalImages - 1)) {
                            nextImage = 0;
                        } else {
                            nextImage = currentImage + 1;
                        }
                        container.find('.single-image:eq(' + currentImage + ')').removeClass('active');
                        container.find('.single-image:eq(' + nextImage + ')').addClass('active');
                        currentImage = nextImage;
                        startingX = x;
                    } else if (x > (startingX + 10)) {
                        if (currentImage == 0) {
                            nextImage = (totalImages - 1);
                        } else {
                            nextImage = currentImage - 1;
                        }
                        container.find('.single-image:eq(' + currentImage + ')').removeClass('active');
                        container.find('.single-image:eq(' + nextImage + ')').addClass('active');
                        currentImage = nextImage;
                        startingX = x;
                    }
                }
            }
            current360container.bind('mouseup touchend', function() {
                dragOn360 = false;
                jQuery(window).unbind("mousemove touchmove");
            });
        });
    })
    window.addEventListener('load', function() {
        if (jQuery(window).width() >= 1200) {
            function Parallax(options) {
                options = options || {};
                this.nameSpaces = {
                    wrapper: options.wrapper || '.parallax',
                    layers: options.layers || '.parallax-layer',
                    deep: options.deep || 'data-parallax-deep'
                };
                this.init = function() {
                    var self = this,
                        parallaxWrappers = document.querySelectorAll(this.nameSpaces.wrapper);
                    for (var i = 0; i < parallaxWrappers.length; i++) {
                        (function(i) {
                            parallaxWrappers[i].addEventListener('mousemove', function(e) {
                                var x = e.clientX,
                                    y = e.clientY,
                                    layers = parallaxWrappers[i].querySelectorAll(self.nameSpaces
                                        .layers);
                                for (var j = 0; j < layers.length; j++) {
                                    (function(j) {
                                        var deep = layers[j].getAttribute(self.nameSpaces.deep),
                                            disallow = layers[j].getAttribute(
                                                'data-parallax-disallow'),
                                            itemX = (disallow && disallow === 'x') ? 0 : x /
                                            deep,
                                            itemY = (disallow && disallow === 'y') ? 0 : y /
                                            deep;
                                        if (disallow && disallow === 'both') return;
                                        layers[j].style.transform = 'translateX(' + itemX +
                                            'px) translateY(' + itemY + 'px)';
                                    })(j);
                                }
                            })
                            parallaxWrappers[i].addEventListener('mouseleave', function(e) {
                                layers = parallaxWrappers[i].querySelectorAll(self.nameSpaces
                                    .layers);
                                for (var j = 0; j < layers.length; j++) {
                                    layers[j].style.transform = 'translate(0,0)';
                                }
                            })
                        })(i);
                    }
                };
                this.init();
                return this;
            }
            new Parallax();
        }
    });
